#include <bits/stdc++.h>
using namespace std;

int main() {

    // integer
    int integer = 10;
    cout << integer << "\n";

    // float
    float decimal = 10.01;
    cout << decimal << "\n";

    // string
    string str;
    getline(cin, str);
    cout << str << "\n";
    
    // character
    char ch;
    cin >> ch;
    cout << ch << "\n";

    return 0;
}
